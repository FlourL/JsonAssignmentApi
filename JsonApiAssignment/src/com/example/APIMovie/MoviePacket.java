package com.example.APIMovie;

public class MoviePacket {
	private int ID;
	private String title;
	MoviePacket(){
		
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
}
